    public static Station findPreviousStation(final Station currentStation, final int lineName, final String direction) {
        switch (lineName) {
            case 1:
                return getPreviousStationFromLine(currentStation, direction, Line1);
            case 2:
                return getPreviousStationFromLine(currentStation, direction, Line2);
            case 3:
                return getPreviousStationFromLine(currentStation, direction, Line3);
            case 4:
                return getPreviousStationFromLine(currentStation, direction, Line4);
            case 5:
                return getPreviousStationFromLine(currentStation, direction, Line5);
        }
        return null;
    }

    private static Station getPreviousStationFromLine(final Station currentStation, final String direction, final Station[] line) {
        final int currentStationPosition = java.util.Arrays.asList(line).indexOf(currentStation);
        final int nextStationPositionOffset = direction.equals("1") ? 1 : -1;
        int nextStationPosition = currentStationPosition + nextStationPositionOffset;

        // Clamp the extreme cases
        nextStationPosition = nextStationPosition < 0 ? 0 : nextStationPosition;
        nextStationPosition = nextStationPosition == line.length ? line.length - 1 : nextStationPosition;

        return line[nextStationPosition];
    }
